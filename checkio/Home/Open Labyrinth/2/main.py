def checkio(data):
    def bfs(matrix, x, y):
        l = [('S', 1, 0), ('N', -1, 0), ('E', 0, 1), ('W', 0, -1)]
        q = [(x, y, '')]
        matrix[x][y] = 1
        while q:
            x, y, s = q.pop(0)
            if x == 10 and y == 10: return s
            for c, i, j in l:
                try:
                    a, b = i+x, y+j
                    if matrix[a][b] == 0:
                        matrix[a][b] = 1
                        q.append((a, b, s+c))
                except:
                    continue
    return bfs(data, 1, 1)

